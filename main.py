    async def get_tweet_content(self, tweet_id):
        """Tweet i√ßeriƒüini al - ƒ∞Yƒ∞LE≈ûTƒ∞Rƒ∞LMƒ∞≈û"""
        try:
            logging.info(f"üìÑ Getting content for tweet: {tweet_id}")
        
            await self.page.goto(f"https://x.com/i/web/status/{tweet_id}", wait_until="domcontentloaded", timeout=30000)
            await asyncio.sleep(3)
        
            # Tweet i√ßeriƒüini bul - birden fazla y√∂ntem
            content = None
        
            # Y√∂ntem 1: Standard tweet text selector
            content_selectors = [
                '[data-testid="tweetText"]',
                'div[data-testid="tweetText"]',
                'article[data-testid="tweet"] [data-testid="tweetText"]'
            ]
        
            for selector in content_selectors:
                try:
                    content_element = await self.page.wait_for_selector(selector, timeout=8000)
                    if content_element:
                        content = await content_element.inner_text()
                        if content and content.strip():
                            logging.info(f"‚úÖ Tweet i√ßeriƒüi bulundu: {content[:100]}...")
                            return content.strip()
                except:
                    continue
        
            # Y√∂ntem 2: Lang attribute ile
            try:
                lang_elements = await self.page.query_selector_all('div[lang]')
                for elem in lang_elements:
                    text = await elem.inner_text()
                    if text and len(text) > 10:  # Minimum content length
                        content = text.strip()
                        logging.info(f"‚úÖ Tweet i√ßeriƒüi bulundu (lang): {content[:100]}...")
                        return content
            except:
                pass
        
            # Y√∂ntem 3: Article i√ßindeki text
            try:
                article = await self.page.query_selector('article[data-testid="tweet"]')
                if article:
                    text_content = await article.inner_text()
                    # Tweet text'ini ayƒ±kla (username, time vs. hari√ß)
                    lines = text_content.split('\n')
                    for line in lines:
                        if len(line) > 20 and not line.startswith('@') and not 'ago' in line:
                            content = line.strip()
                            logging.info(f"‚úÖ Tweet i√ßeriƒüi bulundu (article): {content[:100]}...")
                            return content
            except:
                pass
        
            logging.warning(f"‚ö†Ô∏è Tweet i√ßeriƒüi bulunamadƒ±: {tweet_id}")
            return None
            
        except Exception as e:
            logging.error(f"‚ùå Tweet i√ßeriƒüi alma hatasƒ±: {e}")
            return None

    async def get_tweet_time(self, tweet_id):
        """Tweet'in atƒ±lma zamanƒ±nƒ± al - ƒ∞Yƒ∞LE≈ûTƒ∞Rƒ∞LMƒ∞≈û"""
        try:
            logging.info(f"üïê Getting time for tweet: {tweet_id}")
        
            # Zaten tweet sayfasƒ±ndaysak tekrar gitmeye gerek yok
            current_url = self.page.url
            if f"/status/{tweet_id}" not in current_url:
                await self.page.goto(f"https://x.com/i/web/status/{tweet_id}", wait_until="domcontentloaded", timeout=30000)
                await asyncio.sleep(2)
        
            # Zaman damgasƒ± elementini bul
            time_selectors = [
                'time[datetime]',
                'article time[datetime]',
                '[data-testid="tweet"] time'
            ]
        
            for selector in time_selectors:
                try:
                    time_element = await self.page.wait_for_selector(selector, timeout=8000)
                    if time_element:
                        datetime_str = await time_element.get_attribute('datetime')
                        if datetime_str:
                            tweet_time = datetime.fromisoformat(datetime_str.replace('Z', '+00:00'))
                            logging.info(f"‚úÖ Tweet zamanƒ±: {tweet_time}")
                            return tweet_time
                except:
                    continue
        
            # Alternatif: Relative time'dan √ßƒ±karƒ±m yap
            try:
                time_elements = await self.page.query_selector_all('time')
                for time_elem in time_elements:
                    time_text = await time_elem.inner_text()
                    if 'h' in time_text or 'm' in time_text or 's' in time_text:
                        # Yakla≈üƒ±k zaman hesapla
                        now = datetime.now()
                        if 'h' in time_text:
                            hours = int(time_text.replace('h', '').strip())
                            tweet_time = now - timedelta(hours=hours)
                        elif 'm' in time_text:
                            minutes = int(time_text.replace('m', '').strip())
                            tweet_time = now - timedelta(minutes=minutes)
                        else:
                            tweet_time = now  # Very recent
                    
                        logging.info(f"‚úÖ Tweet zamanƒ± (yakla≈üƒ±k): {tweet_time}")
                        return tweet_time
            except:
                pass
        
            logging.warning(f"‚ö†Ô∏è Tweet zamanƒ± bulunamadƒ±: {tweet_id}")
            # Varsayƒ±lan olarak ≈üu anki zamanƒ± d√∂nd√ºr (1 saat i√ßinde sayƒ±lsƒ±n)
            return datetime.now()
            
        except Exception as e:
            logging.error(f"‚ùå Tweet zamanƒ± alma hatasƒ±: {e}")
            return datetime.now()

